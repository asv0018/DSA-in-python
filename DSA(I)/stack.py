#implementation of stack in python3

stack = []

#adding elements into the stack
stack.append(1)
stack.append(2)
stack.append(3)
stack.append(4)
stack.append(5)

#printing of stack
print("stack : ",str(stack))

#popping of elements from the stack
print("\nAfter popping an element from the stack: ")
print(stack.pop())
print(stack)

print('\n stack after elements are poped:')
print(stack)
